extern crate pacman;

use pacman::Game;
use pacman::Input;

use std::sync::mpsc::channel;
use std::time::Duration;
use std::thread::sleep;


static MAP: &'static str = "
################# ####### #################
#...............# #     # #...............#
#.######.######.# #     # #.######.######.#
#.#...........#.# ####### #.#...........#.#
#.######.######.#         #.######.######.#
#...............  #######  ...............#
#.#############.# #     # #.#############.#
#.............#.# #     # #.#.............#
#.#############.# ####### #.#############.#
#...............#         #...............#
####### ######### ### ### ####### #########
                # #AAAAA# #
####### ######### ####### ####### #########
#...............#         #...............#
#.######.######.# ####### #.######.######.#
#.#...........#.# #     # #.#...........#.#
#.######.######.# #     # #.######.######.#
#...............  #######  ...............#
#.#############.#    <    #.#############.#
#.............#.# ####### #.#.............#
#.#############.# #     # #.#############.#
#...............# #     # #...............#
################# ####### #################
";


fn main()
{
    let (insnk, insrc) = channel();
    let events = Input::new(insnk);
    let _      = events.start();

    let mut game = Game::load(&MAP);

    loop
    {
        let mut keys = Vec::new();

        loop
        {
            if let Ok(key) = insrc.try_recv() {
                keys.push(key);
            } else {
                break;
            }
        }

        if keys.len() == 3 {
            let arrow = (keys[0], keys[1], keys[2]);

            match arrow
            {
                (27, 91, 65) => game.player.pos.x += 1,
                (27, 91, 66) => game.player.pos.x -= 1,
                (27, 91, 67) => game.player.pos.y += 1,
                (27, 91, 68) => game.player.pos.y -= 1,

                _ => ()
            }
        }

        println!("{:?}", keys);

        game.render();

        print!("\x1bc");

        sleep(Duration::from_millis(100));
    }
}
